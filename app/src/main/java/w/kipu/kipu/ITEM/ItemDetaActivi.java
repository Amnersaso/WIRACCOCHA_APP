package w.kipu.kipu.ITEM;

public class ItemDetaActivi {
    String cod,des;

    public ItemDetaActivi(String cod, String des) {
        this.cod = cod;
        this.des = des;
    }

    public String getCod() {
        return cod;
    }

    public void setCod(String cod) {
        this.cod = cod;
    }

    public String getDes() {
        return des;
    }

    public void setDes(String des) {
        this.des = des;
    }
}
